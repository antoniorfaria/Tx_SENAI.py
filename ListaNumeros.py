listaNumeros = [0,6,3,1,5,4]

# Somando todos os valores da lista 

soma= sum(listaNumeros)
print (soma)

#Identificando o maior valor dentro da lista

maiorValor = max (listaNumeros)
print(maiorValor)

#identificando o menor valor dentro da lista 

menorValor = min(listaNumeros)
print (menorValor)

#Ordenar a lista em ordem crescente 

print (listaNumeros.sort())


